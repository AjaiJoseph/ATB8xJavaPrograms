package sept.ex_23092024_Switch_Condition;

public class Lab087 {
    public static void main(String[] args) {
        char code = 'B';
        switch (code){
            default:
                System.out.println("Helloooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
