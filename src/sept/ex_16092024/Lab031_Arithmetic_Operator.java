package sept.ex_16092024;

public class Lab031_Arithmetic_Operator {
    public static void main(String[] args) {
//        Arithmetic Operators
//        + , -, /, *
        int a = 10;
        int b = 2;
        System.out.println(a+b); // + -> Binary - 2 operands needed for this to work
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);

//        % - Modulus - Remainders (balance left values)
        System.out.println(a%b);
    }
}
