package sept.ex_11092024_Data_types;
// DATA TYPES
public class Lab015_DataTypes {
    public static void main(String[] args) {
        byte b = 100; // 8 bites , 1 byte
        short s = 128; // 16 bits, 4 byte
        long phone_number = 8899766554l; // 64 bites, 4 bytes
        long phone_number2 = 8899766554L; // 64 bites, 4 bytes
        char grade = 'A'; // 'A', b, c, d, e, el -> ASCII Value - int - assigned.
        // 16 Bites , 2 Byte
        float f = 3.14f;
        float f2 = 3.14F; // 32 Bites, 4 Byte
        double d = 118.2245245; // 64 Bites, 8 Bytes

//        Non primitive Data types ( User defined)
        String name = "Ajai"; // String - bunch of char (Collection of chars)
//      Array , class- user Data types.
    }
}
